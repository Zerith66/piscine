void    rush00(int *a, int *b)
{
    int	*i;

	i = b;
	while (*i >= 1)
	{
		if (*i == 0 || *i == *b)
		{
			ft_putchar(a, b, i);
			i--;
		}
		while (*i != *b - 1)
		{
			ft_putlim(b, i);
		}
		*i = *i - 1;
	}
}