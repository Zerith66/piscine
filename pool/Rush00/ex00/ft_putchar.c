void    ft_putchar(int *a)
{
    int	o;

	o = 0;
	if (*a > 1)
	{
		write(1, "o", 1);
		o++;
		while (o <= *a - 1)
		{
			write(1, "-", 1);
			o++;
		}
		write(1, "o", 1);
	}
	else
	{
		write(1, "o", 1);
	}
}