int	ft_str_is_numeric(char *str)
{
	int	i;

	i = 0;
	while (*str != '\n')
	{
		if (*str >= 48 && *str <= 57)
		{
			return (1);
		}
		else
		{
			return (0);
		}
	}
	if (*str == '\n')
	{
		return (2);
	}
}
