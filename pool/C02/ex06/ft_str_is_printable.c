int	ft_str_is_printable(char *str)
{
	int	i;

	i = 0;
	while (*str != '\n')
	{
		if (65 <= *str <= 90 || 97 <= *str <= 122 || 57 >= *str >= 48 )
		{
			return (1);
		}
		else
		{
			return (0);
		}
	}
	if (*str == '\n')
	{
		return (2);
	}
}