#include <unistd.h>

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (str[i])
		i++;
	return (i);
}

int	main(int ac, char **av)
{
	if (ac == 1)
	{
		write(1, av[0], ft_strlen(av[0]));
		write(1, "\n", 1);
	}
}
